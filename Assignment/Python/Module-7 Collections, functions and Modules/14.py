# 14) Write a Python program to access values using keys from a dictionary.

my_dict = {
    "name": "Alice",
    "age": 25,
    "city": "New York",
    "job": "Engineer",
    "hobby": "Reading",
    "is_student": False
}

name = my_dict["name"]
age = my_dict["age"]
city = my_dict["city"]
print(f"Name: {name}, Age: {age}, City: {city}")
