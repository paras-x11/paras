# 4. Write a Python program to demonstrate the creation of variables and different data types.

# Creating variables of different data types

# Integer
age = 25
print("Integer:", age)

# Float
height = 5.9
print("Float:", height)

# String
name = "Alice"
print("String:", name)

# Boolean
is_student = True
print("Boolean:", is_student)

# List
subjects = ["Math", "Science", "History"]
print("List:", subjects)

# Tuple
coordinates = (10.5, 20.3)
print("Tuple:", coordinates)

# Dictionary
person_info = {"name": "Alice", "age": 25, "city": "New York"}
print("Dictionary:", person_info)

# Set
unique_numbers = {1, 2, 3, 3, 4}  # Sets automatically remove duplicates
print("Set:", unique_numbers)

# NoneType
nothing = None
print("NoneType:", nothing)
