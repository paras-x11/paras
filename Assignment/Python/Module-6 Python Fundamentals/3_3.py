# 6. Practical Example 2: How to create variables in Python?

# This script demonstrates creating and using variables of different data types.

# Integer variable
age = 30
print("Integer:", age)

# Float variable
height = 5.7
print("Float:", height)

# String variable
name = "John Doe"
print("String:", name)

# Boolean variable
is_student = False
print("Boolean:", is_student)

# List variable
hobbies = ["reading", "swimming", "gaming"]
print("List:", hobbies)

# Tuple variable
coordinates = (40.7128, -74.0060)  # Example: coordinates of New York City
print("Tuple:", coordinates)

# Dictionary variable
person_info = {"name": "John", "age": 30, "city": "New York"}
print("Dictionary:", person_info)

# Set variable
unique_numbers = {1, 2, 3, 4, 5}
print("Set:", unique_numbers)

# NoneType variable
unknown = None
print("NoneType:", unknown)
